userRoutes.get('/', authMiddleware, requireSupport, async (req: Request, res: Response) => {
  try {
    const { 
      page = '1', 
      limit = '10', 
      search, 
      role, 
      status 
    } = req.query

    const pageNum = Math.max(1, parseInt(page as string) || 1)
    const limitNum = Math.min(50, Math.max(1, parseInt(limit as string) || 10))
    const skip = (pageNum - 1) * limitNum

    // Build filter conditions
    const where: any = { AND: [] }

    if (search && typeof search === 'string') {
      const searchTerm = search.trim()
      where.AND.push({
        OR: [
          { username: { contains: searchTerm, mode: 'insensitive' } },
          { fullName: { contains: searchTerm, mode: 'insensitive' } },
          { email: { contains: searchTerm, mode: 'insensitive' } }
        ]
      })
    }

    if (role && role !== 'all') {
      where.AND.push({ role: role as string })
    }

    if (status === 'active') {
      where.AND.push({ isActive: true })
    } else if (status === 'inactive') {
      where.AND.push({ isActive: false })
    }

    const finalWhere = where.AND.length === 0 ? {} : where

    const [users, total] = await Promise.all([
      prisma.user.findMany({
        where: finalWhere,
        select: {
          id: true,
          username: true,
          fullName: true,
          email: true,
          role: true,
          isActive: true,
          createdAt: true,
          lastLogin: true,
          _count: {
            select: {
              assignedTickets: { where: { status: { not: '‡πÄ‡∏™‡∏£‡πá‡∏à‡∏™‡∏¥‡πâ‡∏ô' } } },
              notifications: { where: { isRead: false } }
            }
          }
        },
        orderBy: [{ isActive: 'desc' }, { createdAt: 'desc' }],
        skip,
        take: limitNum
      }),
      prisma.user.count({ where: finalWhere })
    ])

    res.json({
      success: true,
      users,
      pagination: {
        current: pageNum,
        total: Math.ceil(total / limitNum),
        limit: limitNum,
        count: total
      }
    })

  } catch (error) {
    console.error('Get users error:', error)
    res.status(500).json({ 
      success: false,
      error: '‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ' 
    })
  }
})

